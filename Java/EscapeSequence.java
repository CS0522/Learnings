public class EscapeSequence 
{
    public static void main(String[] args)
    {
        System.out.println("Insert 'Tab': \tHello!\t");
        System.out.println("Insert 'Back': \bHello!\b");
        System.out.println("Insert 'New Line': \nHello!\n");
        System.out.println("Insert 'Enter': \rHello!\r");
        System.out.println("Insert 'Change Page': \fHello!\f");
        System.out.println("Insert 'Single Quotation Mark': \'Hello!\'");
        System.out.println("Insert 'Double Quotation Mark': \"Hello!\"");
        System.out.println("Insert 'Back Slash': \\Hello!\\");
    }
}
